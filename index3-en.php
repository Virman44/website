 <p> • First, find out what macro photography and micro photography are.
Macro shooting (from other Greek μακρός - “large”, “large”, and shooting) is a type of film and photography in which the displayed objects are shot at a scale of 1: 1 or larger (according to another version - at a scale of 1: 10 or larger).
The term macro is used more broadly in cases of photographing from closer distances than the distances indicated on the distance scale of camera lenses (the minimum focusing distance for most SLR cameras is about 0.5 meters, for rangefinder lenses - about 1 meter). </ P >

 <p> Micro-photography refers to photographing invisible objects in a greatly enlarged form using a microscope. </p>

<p> First of all, you need to know that, according to the laws of optics, for macro photography of a full-size object, the camera lens must be at a distance equal to its double focal length from the film surface. Thus, if you shoot, for example, with a Zenith or Zorki camera with a lens having a focal length of 5 cm, then for shooting a full-size lens, you must be 10 cm away from the film. When you need to shoot an object with magnification, the lens need to put forward even more.
None of the amateur cameras makes it possible to extend the lens at such a distance, so you can not do without additional accessories, such as a reproduction unit URU, attachment lenses and extension adapter rings.
Since macro photography has to deal with very small objects, the linear dimensions of which are usually not more than 10 mm, small-format cameras, especially Zenith SLR cameras and others, are most convenient for this.
      For micro-photography it is very convenient to use the URU reproduction unit. The microscope is placed on the installation screen and the microscope eyepiece is brought to the objective ring of its lower plate. Further, everything is done in the same way as during reproduction: focus on the frosted glass, and then, moving the top plate of the installation, bring the camera to the microscope and take it off.

Micrography can be done using only the microscope lens. In this case, zooming is obtained due to the very short focal length of the microscope objective at a small distance from it to the subject and a large distance between the lens and the focus plane. Such shooting can only be done with very weak microscope lenses. The linear increase in this case is equal to the ratio of the distance from the lens to the focusing plane to the distance from the lens to the object.

When micrography using the lens and the eyepiece of the microscope, the linear increase will be equal to the product of the magnifications of the lens and the eyepiece. </p>

 <p> • Macro photography is widely used in research laboratories, in geology, paleontology, archeology, biology, forensics and other branches of science and technology.
Macro photography itself is a rather interesting genre, since it allows in many ways to take a fresh look at objects that are commonplace in general. Using a macro lens makes it possible to play with the light and position of objects, as well as select the depth of field. Sometimes the result may look simply fantastic, because many aspects of objects that are usually not fixed by the human eye become available. Macro photography is accessible to everyone, and the main thing here is the ability to detect their high artistic potential in simple, even unimpressive objects. </p>


