 <p> Today, removing flaws, scratches, scuffs, print defects present in a paper photograph (both old and new photographs) is not too difficult. You need to scan the image and process it on the computer using special tools available in the graphics editor, for example, in the PhotoShop editor. Then the photo is saved in digital format, written to disk and, if necessary, printed out. If it is in electronic form, then it is immediately opened on the computer through a specific photo editor. </p>
  

 <p> Also on mobile devices: the main advantage of mobile phones on the Android and IOS operating systems is a huge selection of programs with which it becomes possible to take photos as exclusive and high-quality as possible. The ability to add additional effects to previously taken photographs, the possibility of panoramic shooting, sepia, increased contrast, the predominance of certain colors, the possibility of black and white shooting and much more. </p>

 <p> However, before the photos were retouched. Photo retouching began around 1850, immediately after they appeared. After all, the quality of shooting and printing left much to be desired. Retouchers (this word comes from the French “retoucher”, meaning “touch up,” “paint on”) usually became people with an art education and versed in anatomy. <br>
Most often retouched portrait photographs. All or part of them were made in color. The master could remove the entire background from the negativity of the portrait, and on the positive, substitute or draw any landscape, interior or architectural ensemble as the background. In the second half of the XIX century, photo portraitists not only removed various defects from the pictures, but also “improved” the faces of their clients, for example, removed wrinkles and browned their cheeks. Photo editing techniques have also been widely used. </p>

 <p> Black and white negatives were treated with special scrapers. Hydrochloric, hydrochloric and other acids were used to clarify and reduce the contrast of the photograph, smoothing and blurring the contours on it. Special retouching pencils or the usual soft ones made darkening of certain parts of the image. When using a magnifier. Details were drawn with paints or pencils. Also involved were special retouching devices with glass, optics, a lifting board and backlighting with scattered rays from below. Color negatives were retouched using dyes, graphite, and pencils. In the process, the wizards printed test images to evaluate whether certain actions were successful or undesirable. Of course, in the manufacture of a photograph from a negative, light was inverted. <br>
For retouching and coloring black and white positives, watercolor and aniline paints, airbrushes were used. Drawn flaws on them with thin brushes, pencils.
