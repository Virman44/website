     <div id="content"> 
	<p> To make the simplest pinhole camera, we need: a cardboard box 16x8x7 (you can use a box of tea or juice), translucent paper (parchment or tracing paper), a push pin, glue, scissors and a pencil. </p>
<p> 1. In one of the walls of the box, cut out a rectangle - a window for a 4x5 cm screen. </p>
<p> 2. We seal the window with parchment. </p>
<p> 3. In the center of the opposite wall with a clerical button, we pierce a hole 1 mm in size (then I increased the hole to 5 mm). </p>
<p> 4. In a darkened room we place three candles of different heights in the form of a triangle. </p>
<p> 5. On the screen we get an inverted image of candles. </p>
