	<p> Для изготовления простейшей камеры-обскуры нам потребуется: картонная коробка 16х8х7 (можно использовать коробку от чая или сока), полупрозрачная бумага (пергамент или калька), канцелярская кнопка, клей, ножницы и карандаш.</p>
	<p>1. В одной из стенок коробки вырезаем прямоугольник - окошко для экрана 4х5 см.</p>
	<p>2. Заклеиваем окошко пергаментом.</p>
	<p>3. В центре противоположной стенеке канцелярской кнопкой прокалываем отверстие размером 1мм (затем я увеличил отверстие до 5мм).</p>
	<p>4. В затемненной комнате размещаем три свечи разной высоты в виде треугольника.</p>
	<p>5. На экране получаем перевернутое изображение свечей.</p>
